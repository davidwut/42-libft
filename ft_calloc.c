#include "libft.h"

void	*ft_calloc(size_t n, size_t size)
{
	void	*ptr;

	ptr = malloc(n * size);
	if (ptr == 0)
		return (NULL);
	ft_bzero(ptr, n * size);
	return (ptr);
}
